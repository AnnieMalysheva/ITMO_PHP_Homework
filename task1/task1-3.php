<?php
// вершина 1
$x1 = 1;
$y1 = 1;

// вершина 2
$x2 = 2;
$y2 = 2;

// вершина 3
$x3 = 3;
$y3 = 1;

$a = ($x2 - $x1) ** 2 + ($y2 - $y1) ** 2;
$b = ($x3 - $x2) ** 2 + ($y3 - $y2) ** 2;
$c = ($x1 - $x3) ** 2 + ($y1 - $y3) ** 2;

echo $a + $b == $c || $b + $c == $a || $c + $a == $b ? 'Треугольник прямоугольный' : 'Треугольник не прямоугольный';